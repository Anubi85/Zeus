namespace Zeus.UI.Enums
{
    /// <summary>
    /// Specifies date and time formats for a <see cref="Zeus.UI.Controls.ZeusDateTimePicker"/>.
    /// </summary>
    public enum ZeusDateTimePickerFormats
    {
        /// <summary>
        /// Specifies that the date should be displayed using unabbreviated days of the week and month names and time format is 24 h.
        /// </summary>
        Long,
        /// <summary>
        /// Specifies that the date should be displayed using abbreviated days of the week and month names and time format is 12 h.
        /// </summary>
        Short,
    }
}
